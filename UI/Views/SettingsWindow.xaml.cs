namespace AcmeUI.Views
{
    /// <summary>
    /// An empty window that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class SettingsWindow : Window
    {
        public Frame ContentFrameControl => ContentFrame;  

        public SettingsWindow()
        {
            InitializeComponent();
            this.Closed += (_, __) => App.SettingsWindow = null;
        }

        private void NavView_SelectionChanged(NavigationView sender, NavigationViewSelectionChangedEventArgs args)
        {
            if (args.SelectedItem is NavigationViewItem item)
            {
                switch (item.Tag)
                {
                    case "arduino":
                        ContentFrame.Navigate(typeof(SettingsPages.ArduinoSettingsPage));
                        break;

                    case "local":
                        ContentFrame.Navigate(typeof(SettingsPages.LocalSettingsPage));
                        break;
                }
            }
        }
    }

}
